export function isAppThemeId(value: string): value is AppThemeId {
  return appThemes.some((theme) => theme.id === value);
}

export function getAppTheme(themeId: AppThemeId): ThemeDefinition {
  return appThemes.find((theme) => theme.id === themeId) ?? appThemes[0];
}

export function getStoredAppTheme(): AppThemeId {
  if (globalThis.window === undefined) {
    return DEFAULT_APP_THEME;
  }

  const storedTheme = globalThis.window.localStorage.getItem(
    APP_THEME_STORAGE_KEY,
  );

  if (storedTheme && isAppThemeId(storedTheme)) {
    return storedTheme;
  }

  return DEFAULT_APP_THEME;
}
